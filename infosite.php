<h3>"Чем интересен Буккроссинг?"</h3> 
&nbsp;
<p>"Значит я могу следить за тем, как мои книги живут во всемирной библиотеке? Это круто, но <b>что еще? "</b> <br> 
<br> 
<b>Еще бы! Вы можете... </b> <br> 
<br> 
<b>Регистрироваться</b> во всемирной библиотеке.<br> 
<b>Управлять</b> вашей книжной полкой (как в онлайн-режиме, так и в автономном).<br> 
<b>Создавать</b> индивидуальные наклейки-экслибрисы для ваших книг. <br> 
<b>Охотиться</b> за книгами в вашем районе или во всем мире с помощью страницы "На охоту!"<br> 
<b>Общаться</b> о книгах, авторах и обо всем остальном на форуме.<br> 
<b>Учреждать</b> Официальные Буккроссинг-Зоны (OBCZ) в библиотеке, классе, офисе или кафе.<br> 
<b>Взаимодействовать</b> с членами Буккроссинг-сообщества во всем мире (в онлайне и оффлайне).<br> 
<b>Посещать</b> Буккроссинг-конвенции (в настоящее время проводятся на 5-ти континентах).<br> 
<b>Читать</b> журнальные записи для определения читательских предпочтений и поиска ценных книг.<br> 
<b>Приобретать</b> книги со скидкой.<br> 
<b>Приобщать</b> других буккроссеров к своим книгам.<br> 
<b>Путешествовать</b> по местам, где бывали ваши книги.<br> 
<b>Участвовать</b> в случайном акте Bookcrossing-доброты (RABCK). <br> 
<b>Обучать</b> новичков Буккроссинга.<br> 
<b>Получать</b> наш бюллетень о новых и лучших книгах мира.<br> 
<b>Общаться</b>с нами в MySpace, Twitter, SnapTell и Facebook.<br> 
<b>Дарить</b> своим друзьям в Буккроссинге и в вашей семье виртуальные подарки.<br> 
<b>Участвовать</b> в конкурсах с классными призами.<br> 
<b> Проявлять</b> себя в тематических выпусках.<br> 
<b>Публиковаться</b>, отправив свою статью и фото в нашу рассылку.</p></div>
