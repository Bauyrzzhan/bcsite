    <h1>
        О буккроссинге
    </h1>
        
    <div class="my-4">
        <blockquote style="margin:2em;font-style:italic;">"Книга не только сама является другом, она еще и находит для вас других друзей. Когда вы приняли книгу умом и душой, вы обогатились. Но когда вы передаете ее другим, вы обогащаетесь трижды. " <br><br><p class="right"> - Генри Миллер <br> Книги в моей жизни (1969) </p>
</blockquote>
        <div><h3>"Что <i>такое</i> Буккроссинг?"</h3> 
&nbsp;
<p>Это всемирная библиотека. Это праздник литературы и место, где книги получают новую жизнь. Здесь книге присваивается уникальный идентификатор, предоставляя возможность наблюдать, как книга путешествует из рук в руки. Таким образом проект объединяет читателей всего мира.
В настоящее время <b>1&nbsp;962&nbsp;148 буккроссеров</b> и уже<b> 13&nbsp;115&nbsp;097 книг</b>, путешествующих по разным странам. 
<br>Наше сообщество меняет мир.</p>
&nbsp;
<h3>"Какова наша миссия?"</h3> 
&nbsp;
<p>Очень просто. Мы стремимся связывать людей через книги.</p> 
&nbsp;
<h3>"Как мы это делаем?"</h3> 
&nbsp;
<p><b>Отметь. Передай. Наблюдай.</b> Вдохни новую жизни в книги! Вместо того, чтобы позволять вашим старым любимцам собирать пыль - передавайте их другим читателям. Наш интернет-архив и система наблюдения позволяют участникам взаимодействовать с другими читателями, журналами, обзорной литературой и магазинами и наблюдать через "чтение и выпуск", как изменилась жизнь своих книг. Пользователи имеют возможность помечать и отслеживать свои личные книги, поместив на них BCID (Буккроссинг-идентификатор). BCID - уникальный номер для каждой книги, присваивается на нашем сайте однажды и навсегда. По этому номеру за книгой можно наблюдать. Буккроссинг позволяет <b>бесплатно присоединиться и получать удовольствие.<br></b>Так что будьте смелее с вашими книгами - <b> читайте и передавайте дальше.</b> </p> 
&nbsp;
<h3>"Когда и как это началось?"</h3> 

<p>Ранее были сайты, отслеживающие, например, денежные купюры и фотографии. Деловой партнер Рон Хорнбекер отметил, что ничего не существует для отслеживания книг. В конце концов, книги есть нечто большее, нежели просто материальный объект, и несмотря на то, что они лишены способности к эмоциональной привязанности и глубокому сопереживанию, их не только коллекционируют и благоволят им, но и с их помощью <b>приобщаются</b> к культурному опыту цивилизаций.
Это была точка отсчета проекта... 
Наконец, <b>21 апреля 2001 года</b> Рон, его жена Каори и соучредители Брюс и Хизер Педерсен запустили этот сайт.
Он интересен в первую очередь для распространения и отслеживания книг. Люди, которые им пользуются, имеют возможность взаимодействовать и выстраивать с его помощью конструктивый диалог.</p>
<h3>"Кто такие буккроссеры?"</h3> 
&nbsp;
<p><b> Буккроссеры - люди, которые ценят книги и любят их читать</b>. Они, как правило, <b>самые щедрые, инновационные, экологически ориентированные, добрые, причудливые, веселые и образованные </b> люди на планете. </p> 
&nbsp;
